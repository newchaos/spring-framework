package com.athm;

public class SpringFamilyFactory {

	private String spring_beans = "5.0.x";
	private String spring_context;
	private String spring_aop;
	private String spring_webmvc;
	private String springboot;

	public String getSpring_beans() {
		return spring_beans;
	}

	public void setSpring_beans(String spring_beans) {
		this.spring_beans = spring_beans;
	}

	public String getSpring_context() {
		return spring_context;
	}

	public void setSpring_context(String spring_context) {
		this.spring_context = spring_context;
	}

	public String getSpring_aop() {
		return spring_aop;
	}

	public void setSpring_aop(String spring_aop) {
		this.spring_aop = spring_aop;
	}

	public String getSpring_webmvc() {
		return spring_webmvc;
	}

	public void setSpring_webmvc(String spring_webmvc) {
		this.spring_webmvc = spring_webmvc;
	}

	public String getSpringboot() {
		return springboot;
	}

	public void setSpringboot(String springboot) {
		this.springboot = springboot;
	}
}
